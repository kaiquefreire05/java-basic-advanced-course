package entities;

public class PeopleOlder {
	private int age;
	private String name;
	
	
	// Construtores da classe
	public PeopleOlder(int age, String name) {
		super();
		this.age = age;
		this.name = name;
	}

	
	//GETTERS E SETTERS
	
	public int getAge() {
		return age;
	}


	public void setAge(int age) {
		this.age = age;
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}
	
	// FUNÇÕES DO OBJETO
	
	
	
}
